// A variable is an object in our code that is assigned a value and can be changed depending on its use.
// This is done by declaring the identified object as a variable like shown below.
var apple = 2;
// whereby; apple is our object that is assigned value, 2

// A constant is an object in our code that is assigned a value once and can never be changed.
// This is done by declaring the identified object as a constant as shown below.
const apple = 2;